        return 5
    }
    
    func collectionView(_ collectionView: UICollectionView, cellForItemAt indexPath: IndexPath) -> UICollectionViewCell {
        let cell = collectionView.dequeueReusableCell(withReuseIdentifier: "CeldaColeccion", for: indexPath) as! CeldaCollectionViewCell
        return cell
    }
    
    //UIColllectionViewDelegateFlowLayout
